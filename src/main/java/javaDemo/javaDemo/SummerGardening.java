package javaDemo;

public class SummerGardening {

	public static void main(String[] args) {
		
		source();
		SummerGardening obj = new SummerGardening ();
		obj.organic();
		obj.love();
		obj.babies();
		
		
	}

    public static void source () {
    	System.out.println("Summer gardening is a great source of fresh vegetables.");
      }
	private void organic () {
		System.out.println("Every year we get organic vegetables from our summer garden.");
	}
	void love () {
		System.out.println("All of my family members loves to work together in our summer garden.");
	}
	protected void babies () {
		System.out.println("My babies loves the humming birds in our summer garden.");
	}
	
	
	
	
	
	
	
	
	
	
	
}
